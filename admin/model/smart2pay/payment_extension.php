    /**
     * Uninstall database schema
     */
    public function uninstall()
    {
        $this->load->model('setting/setting');
        $this->model_setting_setting->deleteSetting('smart2pay');

        $this->db->query("DROP TABLE IF EXISTS `" . DB_PREFIX . "smart2pay_log`");
        $this->db->query("DROP TABLE IF EXISTS `" . DB_PREFIX . "smart2pay_method`");
        $this->db->query("DROP TABLE IF EXISTS `" . DB_PREFIX . "smart2pay_country`");
        $this->db->query("DROP TABLE IF EXISTS `" . DB_PREFIX . "smart2pay_country_method`");

        return;

        /*
         * Uninstall checkout helper
         */
        $this->db->query("
            DELETE FROM `" . DB_PREFIX . "extension` WHERE `type` = 'payment' AND `code` = 'smart2pay_checkout_helper'
        ");
        $this->model_setting_setting->deleteSetting('smart2pay_checkout_helper');
    }


